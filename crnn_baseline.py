"""## Baseline Architecture — Why CRNN?

### Why not a plain CNN classifier?
A fixed-length output (6 characters) means we could theoretically use
6 independent CNN classifiers — one per position. But this ignores
sequential context. When a character is partially occluded, neighboring
characters provide strong contextual cues. A sequential model can use
"the character after this blob looks like N, and the partial stroke
before it is consistent with D" — a plain classifier cannot.

### The CRNN architecture
**CNN (5 convolutional blocks):**  
Extracts visual features from the image column by column. Each block
applies Conv2d → BatchNorm → ReLU → MaxPool. The image (1×32×128) is
progressively compressed to (256×1×32), producing 32 feature vectors —
one per horizontal time step. Each time step roughly corresponds to a
vertical slice of the image.

**Bidirectional LSTM (2 layers, 256 hidden units):**  
Reads the 32 feature columns both left-to-right and right-to-left
simultaneously. Bidirectionality is important — the model can use
context from the right side of the sequence to resolve ambiguity on
the left, and vice versa. For occluded characters, this is critical.

**CTC Loss (Connectionist Temporal Classification):**  
Standard cross-entropy requires exact character-level alignment between
input positions and output labels. CTC does not — it learns the alignment
automatically by summing over all valid alignments during training. This
is essential when character positions vary due to distortion and irregular
spacing.

### Training observations
The model exhibited a characteristic CTC training pattern — a long plateau
(epochs 1-18, CER ~0.96) followed by a sudden breakthrough at epoch 19
(CER dropped to 0.87). This plateau occurs because CTC must simultaneously
learn feature extraction AND sequence alignment, creating a difficult
optimization landscape. Once the model finds a viable alignment strategy,
improvement is rapid. Final baseline CER: **0.0208**.
"""

class CRNN(nn.Module):
    def __init__(self, num_classes):
        super(CRNN, self).__init__()

        self.cnn = nn.Sequential(
            nn.Conv2d(1, 32, kernel_size=3, padding=1),
            nn.BatchNorm2d(32),
            nn.ReLU(),
            nn.MaxPool2d(2, 2),

            nn.Conv2d(32, 64, kernel_size=3, padding=1),
            nn.BatchNorm2d(64),
            nn.ReLU(),
            nn.MaxPool2d(2, 2),

            nn.Conv2d(64, 128, kernel_size=3, padding=1),
            nn.BatchNorm2d(128),
            nn.ReLU(),
            nn.MaxPool2d((2,1), (2,1)),

            nn.Conv2d(128, 256, kernel_size=3, padding=1),
            nn.BatchNorm2d(256),
            nn.ReLU(),
            nn.MaxPool2d((2,1), (2,1)),

            nn.Conv2d(256, 256, kernel_size=3, padding=1),
            nn.BatchNorm2d(256),
            nn.ReLU(),
            nn.MaxPool2d((2,1), (2,1)),
        )

        self.rnn = nn.LSTM(256, 256, num_layers=2,
                           batch_first=True,
                           bidirectional=True,
                           dropout=0.3)

        self.fc = nn.Linear(512, num_classes)

    def forward(self, x):
        features = self.cnn(x)
        b, c, h, w = features.size()
        features = features.squeeze(2)
        features = features.permute(0, 2, 1)
        output, _ = self.rnn(features)
        output = self.fc(output)
        output = output.permute(1, 0, 2)
        return output

