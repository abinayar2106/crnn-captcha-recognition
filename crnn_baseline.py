"""## Hypothesis — Why Attention?

The error analysis revealed a clear pattern: the model's mistakes
are concentrated in characters occluded by the black blob.
The baseline BiLSTM treats all 32 time steps equally — it has no
mechanism to say "this region of the image matters more than that one."

**Hypothesis:** Adding an attention mechanism on top of the BiLSTM
will allow the model to upweight time steps corresponding to visible
character regions and downweight blob-dominated regions, reducing
confusion in occluded areas.

**Implementation:** A lightweight additive attention layer with a
residual connection. The residual is critical — it ensures stable
gradient flow during early training when attention weights are random.
"""

class Attention(nn.Module):
    def __init__(self, hidden_size):
        super(Attention, self).__init__()
        self.attention = nn.Linear(hidden_size, 1)

    def forward(self, lstm_output):
        # lstm_output: (batch, time_steps, hidden_size)
        weights = self.attention(lstm_output)        # (batch, time_steps, 1)
        weights = torch.softmax(weights, dim=1)      # normalize across time steps
        context = lstm_output * weights              # weighted hidden states
        return context, weights


class AttentionCRNN(nn.Module):
    def __init__(self, num_classes):
        super(AttentionCRNN, self).__init__()

        # Same CNN as before
        self.cnn = nn.Sequential(
            nn.Conv2d(1, 32, kernel_size=3, padding=1),
            nn.BatchNorm2d(32),
            nn.ReLU(),
            nn.MaxPool2d(2, 2),

            nn.Conv2d(32, 64, kernel_size=3, padding=1),
            nn.BatchNorm2d(64),
            nn.ReLU(),
            nn.MaxPool2d(2, 2),

            nn.Conv2d(64, 128, kernel_size=3, padding=1),
            nn.BatchNorm2d(128),
            nn.ReLU(),
            nn.MaxPool2d((2,1), (2,1)),

            nn.Conv2d(128, 256, kernel_size=3, padding=1),
            nn.BatchNorm2d(256),
            nn.ReLU(),
            nn.MaxPool2d((2,1), (2,1)),

            nn.Conv2d(256, 256, kernel_size=3, padding=1),
            nn.BatchNorm2d(256),
            nn.ReLU(),
            nn.MaxPool2d((2,1), (2,1)),
        )

        # Same BiLSTM
        self.rnn = nn.LSTM(256, 256, num_layers=2,
                           batch_first=True,
                           bidirectional=True,
                           dropout=0.3)

        # NEW — attention layer
        self.attention = Attention(512)  # 512 = 256*2 bidirectional

        # NEW — slightly wider FC to compensate for attention compression
        self.fc = nn.Linear(512, num_classes)

    def forward(self, x):
    # CNN
      features = self.cnn(x)
      b, c, h, w = features.size()
      features = features.squeeze(2)
      features = features.permute(0, 2, 1)   # (batch, 32, 256)

    # BiLSTM
      lstm_out, _ = self.rnn(features)        # (batch, 32, 512)

    # Attention with residual connection
      context, attn_weights = self.attention(lstm_out)  # (batch, 32, 512)
      context = context + lstm_out            # RESIDUAL — add original back

    # FC
      output = self.fc(context)               # (batch, 32, num_classes)
      output = output.permute(1, 0, 2)        # CTC format

      return output, attn_weights


